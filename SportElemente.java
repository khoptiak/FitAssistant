package com.project.khopt.fitassistant;

public class SportElemente {

    private String url;
    private String name;

    public SportElemente(String url, String name) {
        this.url = url;
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public String getName() {
        return name;
    }
}
